SET ECHO ON;
SET SERVEROUTPUT ON;
WHENEVER SQLERROR EXIT SQL.SQLCODE;

ALTER SESSION SET CONTAINER = CCGLPDB;
SHOW CON_NAME;

CREATE OR REPLACE DIRECTORY DPUMP_MIG_DIR AS 'E:\project\myitsm\src\docs\dpump';
GRANT DATAPUMP_EXP_FULL_DATABASE TO CCGL;
GRANT EXP_FULL_DATABASE TO CCGL;
GRANT READ, WRITE ON DIRECTORY DPUMP_MIG_DIR TO CCGL;

COLUMN granted_role FORMAT A35;
SELECT granted_role
FROM dba_role_privs
WHERE grantee = 'CCGL'
  AND granted_role IN ('DATAPUMP_EXP_FULL_DATABASE','EXP_FULL_DATABASE')
ORDER BY granted_role;

SELECT privilege
FROM dba_tab_privs
WHERE owner = 'SYS'
  AND table_name = 'DPUMP_MIG_DIR'
  AND grantee = 'CCGL'
ORDER BY privilege;

EXIT;
