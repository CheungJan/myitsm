-- 文件说明：创建独立迁移PDB（CCGL_MIG）
-- 执行身份：SYSDBA（CDB根容器）
-- 注意：请根据本机目录结构调整 FILE_NAME_CONVERT 路径。
--       ORA-65005 通常是因为路径写成了 PDBORCL，而不是 PDB$SEED 的真实路径。

SET ECHO ON;
SET SERVEROUTPUT ON;

-- 1) 切换到CDB根容器
ALTER SESSION SET CONTAINER = CDB$ROOT;

-- 1.1) 先查看 PDB$SEED 数据文件真实路径（按输出结果调整下方 FILE_NAME_CONVERT）
COLUMN name FORMAT A120;
SELECT name
FROM v$datafile
WHERE con_id = 2;

-- 2) 创建PDB（管理员账号仅用于PDB内部管理，不用于业务）
CREATE PLUGGABLE DATABASE CCGL_MIG
  ADMIN USER pdb_admin IDENTIFIED BY "PdbAdmin_ChangeMe_2026"
  FILE_NAME_CONVERT = (
    'D:\APP\CHEUNGJAN\ORADATA\ORCL\PDBSEED\',
    'D:\APP\CHEUNGJAN\ORADATA\ORCL\CCGL_MIG\'
  );

-- 3) 打开PDB并保存启动状态
ALTER PLUGGABLE DATABASE CCGL_MIG OPEN;
-- 说明：部分 12.1 环境对 SAVE STATE 兼容性存在差异，若报错可先跳过。
-- ALTER PLUGGABLE DATABASE CCGL_MIG SAVE STATE;

-- 4) 核验PDB状态
COLUMN NAME FORMAT A20;
COLUMN OPEN_MODE FORMAT A15;
SELECT NAME, OPEN_MODE FROM V$PDBS WHERE NAME = 'CCGL_MIG';

-- 5) 建议：检查服务是否可用（若未自动注册，需在listener侧处理）
COLUMN NAME FORMAT A30;
SELECT NAME, PDB, NETWORK_NAME
FROM CDB_SERVICES
WHERE PDB = 'CCGL_MIG';

