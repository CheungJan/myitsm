SET ECHO ON;
SET SERVEROUTPUT ON;
WHENEVER SQLERROR EXIT SQL.SQLCODE;

BEGIN
  EXECUTE IMMEDIATE 'DROP DATABASE LINK CCGLPDB_LINK';
EXCEPTION
  WHEN OTHERS THEN
    IF SQLCODE != -2024 THEN
      RAISE;
    END IF;
END;
/

CREATE DATABASE LINK CCGLPDB_LINK
  CONNECT TO ccgl IDENTIFIED BY "ccgl"
  USING 'localhost:1521/ccglpdb';

SELECT SYS_CONTEXT('USERENV','CON_NAME') AS con_name,
       SYS_CONTEXT('USERENV','SESSION_USER') AS session_user
FROM dual;

SELECT COUNT(*) AS source_tables
FROM user_tables@CCGLPDB_LINK;

EXIT;
