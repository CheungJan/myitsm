PROMPT === 2) 批量迁移剩余8个对象（FULL LOAD） ===
DECLARE
  v_sql CLOB;
BEGIN
  FOR r IN (
    SELECT 'TIT05_REPAIRINFO' AS src_name FROM dual
    UNION ALL SELECT 'TIT06_USERAREA' FROM dual
    UNION ALL SELECT 'TIT10_MAINTENANCEDAY' FROM dual
    UNION ALL SELECT 'TIT10_MAINTENANCE_LIABILITY' FROM dual
    UNION ALL SELECT 'TIT10_MAIN_TRACK' FROM dual
    UNION ALL SELECT 'TIT10_POS_DETAIL' FROM dual
    UNION ALL SELECT 'TIT11_MAINTENANCE_ATTC' FROM dual
    UNION ALL SELECT 'TIT12_MAINTENANCE_ARCHIVE' FROM dual
  ) LOOP
    BEGIN
      v_sql := 'CREATE TABLE ITSM_CORE_' || r.src_name || ' AS SELECT * FROM ' || r.src_name || ' WHERE 1=0';
      EXECUTE IMMEDIATE v_sql;
    EXCEPTION
      WHEN OTHERS THEN
        IF SQLCODE != -955 THEN
          RAISE;
        END IF;
    END;

    v_sql := 'TRUNCATE TABLE ITSM_CORE_' || r.src_name;
    EXECUTE IMMEDIATE v_sql;

    v_sql := 'INSERT INTO ITSM_CORE_' || r.src_name || ' SELECT * FROM ' || r.src_name;
    EXECUTE IMMEDIATE v_sql;

    COMMIT;
  END LOOP;
END;
