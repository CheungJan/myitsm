PROMPT === 4) 状态回写（日志 + 工作包） ===
MERGE INTO MIG_P1_BATCH_LOG t
USING (
  SELECT 'P1_BATCH03' AS batch_no, 'TABLE' AS object_type, 'TIT05_REPAIRINFO' AS object_name, 'STEP2_BULK_MIGRATE' AS action_type, 'DONE' AS execute_status, 'bulk full-load to ITSM_CORE_TIT05_REPAIRINFO' AS note FROM dual
  UNION ALL SELECT 'P1_BATCH03','TABLE','TIT06_USERAREA','STEP2_BULK_MIGRATE','DONE','bulk full-load to ITSM_CORE_TIT06_USERAREA' FROM dual
  UNION ALL SELECT 'P1_BATCH03','TABLE','TIT10_MAINTENANCEDAY','STEP2_BULK_MIGRATE','DONE','bulk full-load to ITSM_CORE_TIT10_MAINTENANCEDAY' FROM dual
  UNION ALL SELECT 'P1_BATCH03','TABLE','TIT10_MAINTENANCE_LIABILITY','STEP2_BULK_MIGRATE','DONE','bulk full-load to ITSM_CORE_TIT10_MAINTENANCE_LIABILITY' FROM dual
  UNION ALL SELECT 'P1_BATCH03','TABLE','TIT10_MAIN_TRACK','STEP2_BULK_MIGRATE','DONE','bulk full-load to ITSM_CORE_TIT10_MAIN_TRACK' FROM dual
  UNION ALL SELECT 'P1_BATCH03','TABLE','TIT10_POS_DETAIL','STEP2_BULK_MIGRATE','DONE','bulk full-load to ITSM_CORE_TIT10_POS_DETAIL' FROM dual
  UNION ALL SELECT 'P1_BATCH03','TABLE','TIT11_MAINTENANCE_ATTC','STEP2_BULK_MIGRATE','DONE','bulk full-load to ITSM_CORE_TIT11_MAINTENANCE_ATTC' FROM dual
  UNION ALL SELECT 'P1_BATCH03','TABLE','TIT12_MAINTENANCE_ARCHIVE','STEP2_BULK_MIGRATE','DONE','bulk full-load to ITSM_CORE_TIT12_MAINTENANCE_ARCHIVE' FROM dual
) s
ON (
  t.batch_no = s.batch_no
  AND t.object_type = s.object_type
  AND t.object_name = s.object_name
  AND t.action_type = s.action_type
)
WHEN MATCHED THEN
  UPDATE SET t.execute_status = s.execute_status, t.note = s.note, t.executed_at = SYSDATE
WHEN NOT MATCHED THEN
  INSERT (batch_no, object_type, object_name, action_type, execute_status, note, executed_at)
  VALUES (s.batch_no, s.object_type, s.object_name, s.action_type, s.execute_status, s.note, SYSDATE);
