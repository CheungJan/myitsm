SET DEFINE OFF;
SET SERVEROUTPUT ON;
SET FEEDBACK ON;

PROMPT [STEP-1] 初始化历史基线数据（仅插入满足约束的数据）

INSERT INTO TMM22_CUSTOMERS_HISTORY (
    CUSTCD,
    CUSTCARD,
    CUSTNM,
    STORE_ID,
    CHANGE_TYPE,
    OLD_CUSTCARD,
    DEVICE_CHANGE_ID,
    OPER_CD,
    USEFLG,
    CREATE_TIME,
    UPDATE_TIME,
    CHANGE_REASON,
    NEW_CUSTCARD,
    CHANGE_TIME
)
SELECT
    t.CUSTCD,
    t.CUSTCARD,
    SUBSTR(t.CUSTNM, 1, 80) AS CUSTNM,
    t.CUSTCD AS STORE_ID,
    'INIT' AS CHANGE_TYPE,
    NULL AS OLD_CUSTCARD,
    NULL AS DEVICE_CHANGE_ID,
    'SYSTEM' AS OPER_CD,
    '1' AS USEFLG,
    SYSDATE AS CREATE_TIME,
    SYSDATE AS UPDATE_TIME,
    'System baseline init' AS CHANGE_REASON,
    t.CUSTCARD AS NEW_CUSTCARD,
    SYSDATE AS CHANGE_TIME
FROM TMM22_CUSTOMERS t
WHERE t.USEFLG = '1'
  AND t.CUSTCARD IS NOT NULL
  AND NOT EXISTS (
      SELECT 1
      FROM TMM22_CUSTOMERS_HISTORY h
      WHERE h.CUSTCD = t.CUSTCD
        AND h.CHANGE_TYPE = 'INIT'
  );

COMMIT;

